public class loops {
    public static void main(String[] args){

        // Loop for
        int[] n = new int[5];

        n[0] = 0;
        n[1] = 5;
        n[2] = 10;
        n[3] = 15;
        n[4] = 20;


        for (int i = 0; i < n.length; i++){
            System.out.println(n[i]);
        }


    } 
}
